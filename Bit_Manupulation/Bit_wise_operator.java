import java.util.*;

public class Bit_wise_operator {
    public static void main(String[] args) {
        System.out.println(5 & 6); // dont use && as its logical operator
        System.out.println(5 | 6);// Binary OR
        System.out.println(5 ^ 6);// Binary XOR
        System.out.println(~5);// Binary 1'S complement (not)
        System.out.println(~0);// Binary 1'S complement (not)
        System.out.println(~1);// Binary 1'S complement (not)
    }
}